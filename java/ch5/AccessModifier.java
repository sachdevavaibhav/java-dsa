package ch5;

public class AccessModifier {
    /*
     * What is access modifier?
     * - Access modifiers are keywords used to specify the accessibility (visibility) of a class, data
     * member, or method.
     *
     * What are the different types of access modifiers?
     * - public: accessible in all classes
     * - protected: accessible within the same package or subclasses in different packages
     * - default: accessible within the same package
     * - private: accessible within the same class
     *
     * What is the default access modifier?
     * - If you do not specify any access modifier, it will be the default access modifier.
     */

}
